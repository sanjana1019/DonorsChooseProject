
public class PovertyType {
	

	private String range;

	/**
	 * @return the range
	 */
	public String getRange() {
		return range;
	}

	/**
	 * sets the range
	 * @param range
	 */
	public void setRange(String range) {
		this.range = range;
	}

	/**
	 * displays the range
	 */
	@Override
	public String toString() {
		return "PovertyType [range=" + range + "]";
	} 
}
